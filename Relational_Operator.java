package com.darshan.javapracticeprogram;

public class Relational_Operator {
	
	public static void  main(String args[]) {
		
	int numOne = 20;
	int numTwo = 10;
	
	System.out.println(numOne > numTwo);
	
	System.out.println(numOne >= numTwo);
	
	System.out.println(numOne < numTwo);
	
	System.out.println(numOne <= numTwo);
	
	
	}

}
